
public class Calculadora {
    
    static float soma(float a, float b){
    float soma = a+b;
    return soma;
    }

    static float subtrair(float a, float b){
        float subtrair = a-b;
        return subtrair;
    }
    static float multiplicar(float a , float b){
        float multiplicar = a*b;
        return multiplicar;
    }
    static float dividir(float a, float b){
        float dividir = a/b;
        return dividir;
    }
}
